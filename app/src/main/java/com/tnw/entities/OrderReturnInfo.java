package com.tnw.entities;

public class OrderReturnInfo {
	
	private String 
	returnId,
    typeName,
    money,
    buyerContent,
    sellerContent,
    checkName,
    statusName,
    createTime,
    finishTime;

	public String getReturnId() {
		return returnId;
	}

	public void setReturnId(String returnId) {
		this.returnId = returnId;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getMoney() {
		return money;
	}

	public void setMoney(String money) {
		this.money = money;
	}

	public String getBuyerContent() {
		return buyerContent;
	}

	public void setBuyerContent(String buyerContent) {
		this.buyerContent = buyerContent;
	}

	public String getSellerContent() {
		return sellerContent;
	}

	public void setSellerContent(String sellerContent) {
		this.sellerContent = sellerContent;
	}

	public String getCheckName() {
		return checkName;
	}

	public void setCheckName(String checkName) {
		this.checkName = checkName;
	}

	public String getStatusName() {
		return statusName;
	}

	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getFinishTime() {
		return finishTime;
	}

	public void setFinishTime(String finishTime) {
		this.finishTime = finishTime;
	}
	
	
	

}
