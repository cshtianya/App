package com.tnw.entities;

public class ProductTradeRecord {
	
	private String itemId, itemName, itemCommodityName, itemFirstName,
	itemFirstValue, itemSecondName, itemSecondValue, itemPrice,
	itemStock, itemDate;

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getItemCommodityName() {
		return itemCommodityName;
	}

	public void setItemCommodityName(String itemCommodityName) {
		this.itemCommodityName = itemCommodityName;
	}

	public String getItemFirstName() {
		return itemFirstName;
	}

	public void setItemFirstName(String itemFirstName) {
		this.itemFirstName = itemFirstName;
	}

	public String getItemFirstValue() {
		return itemFirstValue;
	}

	public void setItemFirstValue(String itemFirstValue) {
		this.itemFirstValue = itemFirstValue;
	}

	public String getItemSecondName() {
		return itemSecondName;
	}

	public void setItemSecondName(String itemSecondName) {
		this.itemSecondName = itemSecondName;
	}

	public String getItemSecondValue() {
		return itemSecondValue;
	}

	public void setItemSecondValue(String itemSecondValue) {
		this.itemSecondValue = itemSecondValue;
	}

	public String getItemPrice() {
		return itemPrice;
	}

	public void setItemPrice(String itemPrice) {
		this.itemPrice = itemPrice;
	}

	public String getItemStock() {
		return itemStock;
	}

	public void setItemStock(String itemStock) {
		this.itemStock = itemStock;
	}

	public String getItemDate() {
		return itemDate;
	}

	public void setItemDate(String itemDate) {
		this.itemDate = itemDate;
	}
	
	
	
}
