package com.tnw.entities;

public class CommentNode {
	
	private String itemId, itemUserId, itemUserName, itemUserImage,
	itemContent, itemDate;

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public String getItemUserId() {
		return itemUserId;
	}

	public void setItemUserId(String itemUserId) {
		this.itemUserId = itemUserId;
	}

	public String getItemUserName() {
		return itemUserName;
	}

	public void setItemUserName(String itemUserName) {
		this.itemUserName = itemUserName;
	}

	public String getItemUserImage() {
		return itemUserImage;
	}

	public void setItemUserImage(String itemUserImage) {
		this.itemUserImage = itemUserImage;
	}

	public String getItemContent() {
		return itemContent;
	}

	public void setItemContent(String itemContent) {
		this.itemContent = itemContent;
	}

	public String getItemDate() {
		return itemDate;
	}

	public void setItemDate(String itemDate) {
		this.itemDate = itemDate;
	}
	
	
}
